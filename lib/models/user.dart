class User {
  final String firstName;
  final String lastName;
  final String email;
  final String phone;
  final String address;
  final String job;
  final DateTime birthDate;

  User({
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phone,
    required this.address,
    required this.job,
    required this.birthDate,
  });
}